#!/usr/bin/env python3
"""Ops dashboard generator — revenue lanes + system health in one static page.

Same pattern as tools/cost_page.py: renders one self-contained dark-theme HTML
file (frontends/ops.html) with all data baked in; works over file://, no server.
Regenerated by launchd job com.claude-stack.ops-dashboard every 5 min.

Tabs:
  1. REVENUE — every revenue lane (Stripe live numbers, Gumroad, Hun Vault,
     Intent Lead Finder, lead lists, intent strike list, persona1/social, content
     channels) + costs / ROI / savings (reuses tools.spend_dashboard).
  2. SYSTEM HEALTH — launchd jobs, endpoint up/down, diagnostician summary,
     spend breakers, disk, e2e/smoke.

READ-ONLY everywhere (Stripe GETs only). Every section degrades to
'unavailable' instead of crashing. No secrets are written into the HTML.

    PYTHONPATH=. .venv/bin/python tools/ops_dashboard.py [--print]
"""
