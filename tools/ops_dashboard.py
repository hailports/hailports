    lanes_tbl = _table(["Lane", "Status", "Revenue to date", "Detail", "Blocker"], lane_rows)

    # costs / ROI / savings detail
    today = can.get("today", {}) if "_error" not in can else {}
    wk = report.get("seven_day", {}) or {}
    savings_rows = [
        f'<tr><td class="k">Paid API spend today</td><td>{_money(today.get("api_cost", 0))}</td></tr>',
        f'<tr><td class="k">Paid API spend 7d</td><td>{_money(wk.get("paid_spend", 0))}</td></tr>',
        f'<tr><td class="k">Paid API spend all-time</td><td>{_money(spend_all)} ({alltime.get("api_calls", 0)} calls)</td></tr>',
        f'<tr><td class="k">Monthly cost run-rate</td><td>{_money(monthly_cost)}/mo</td></tr>',
        f'<tr><td class="k">Gross cost avoided (all-time, est.)</td><td>{_money(alltime.get("avoided_cost", 0))}</td></tr>',
        f'<tr><td class="k">Net saved (all-time, est.)</td><td><b class="good">{_money(saved)}</b> over {alltime.get("days_active", 0)} days</td></tr>',
        f'<tr><td class="k">Savings vs $100/mo max plan (est.)</td><td>{_money(vs_max.get("savings_vs_max", 0))}</td></tr>',
    ]
    if lifetime.get("scenarios"):
        lk = lifetime["scenarios"].get("likely", {})
        savings_rows.append(
            f'<tr><td class="k">Lifetime value, "likely" scenario (est.)</td>'
            f'<td>{_money(lk.get("total_value", 0))}</td></tr>')
