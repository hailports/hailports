def collect_stripe() -> dict:
    key = None
    try:
        for line in (BASE_DIR / ".env").read_text().splitlines():
            if line.startswith("STRIPE_SECRET_KEY="):
                key = line.split("=", 1)[1].strip()
                break
    except Exception:
        pass
    if not key:
        return {"error": "no STRIPE_SECRET_KEY in .env"}

    def get(path):
        req = urllib.request.Request("https://api.stripe.com/v1/" + path)
        req.add_header("Authorization", "Bearer " + key)
        with urllib.request.urlopen(req, timeout=15) as r:
            return json.load(r)

    out = {}
    try:
        bal = get("balance")
        out["available"] = sum(x["amount"] for x in bal.get("available", [])) / 100.0
        out["pending"] = sum(x["amount"] for x in bal.get("pending", [])) / 100.0
    except Exception as e:
        out["balance_error"] = str(e)[:120]

    try:
        charges, starting_after = [], None
        for _ in range(5):  # cap pagination: 500 charges max
            q = "charges?limit=100" + (f"&starting_after={starting_after}" if starting_after else "")
            page = get(q)
            charges.extend(page.get("data", []))
            if not page.get("has_more"):
                break
            starting_after = charges[-1]["id"]
        succ = [c for c in charges if c.get("status") == "succeeded" and not c.get("refunded")]
        cutoff = (datetime.now(timezone.utc) - timedelta(days=30)).timestamp()
        out["charges_seen"] = len(charges)
        out["revenue_alltime"] = sum(c["amount"] for c in succ) / 100.0
        out["revenue_30d"] = sum(c["amount"] for c in succ if c.get("created", 0) >= cutoff) / 100.0
        out["succeeded_count"] = len(succ)
    except Exception as e:
        out["charges_error"] = str(e)[:120]

    try:
        sess = get("checkout/sessions?limit=100")
        data = sess.get("data", [])
        out["checkout_sessions_recent"] = len(data)
        out["checkout_completed"] = sum(1 for s in data if s.get("status") == "complete")
    except Exception as e:
        out["sessions_error"] = str(e)[:120]
    return out


