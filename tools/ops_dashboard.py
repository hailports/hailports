def _esc(s) -> str:
    return html.escape(str(s))


def _money(v) -> str:
    try:
        v = float(v or 0)
    except Exception:
        v = 0.0
    return f"${v:,.2f}"


def _read_json(p: Path):
    try:
        return json.loads(p.read_text())
    except Exception:
        return None


