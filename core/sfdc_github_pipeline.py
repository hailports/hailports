#!/usr/bin/env python3
"""GitHub loop for the WORK-lane SFDC engineer — open PRs, read review feedback, stage prod.

The engineer works ENTIRELY on feature branches: it opens a PR against a working base
(develop / an integration branch), reads back the review comments (human + Copilot/Cursor
bots), and STAGES a prod-promotion summary for Operator's real gated CompanyA pipeline. It never
merges, never force-pushes, and never promotes to prod on its own.

Design rules baked in:
  - **Deterministic-first.** Bot detection, base-branch guarding, and scrubbing are pure logic
    so recurring PR mechanics never burn a premium AI call.
  - **DRY-RUN by default.** Every git/gh MUTATION returns the exact command it WOULD run; it
    only executes when dry=False. Reads (review feedback) run live — they're safe + free.
  - **Zero AI/stack trace on employer surfaces.** `_scrub` hard-rejects any title/body carrying
    a Claude/stack/assistant/generated-by/bot marker before it can reach a CompanyA PR.
  - **Protected branches are untouchable.** A PR is never based on (nor a head of) main / master
    / prod; open_pr refuses.

This module carries no AI/stack trace itself and authors no SF-facing content.
"""
from __future__ import annotations

import json
import os
import shlex
import subprocess
from typing import Any

